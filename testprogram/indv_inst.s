.text
.globl main

main:
    # =================================================================
    # ADD - Test overflow case
    # =================================================================
    lui $t0, 0x7FFF        # $t0 = 0x7FFF0000
    ori $t0, $t0, 0xFFFF   # $t0 = 0x7FFFFFFF (MAX_INT)
    addi $t1, $zero, 1     # $t1 = 1
    add $t2, $t0, $t1      # Overflow: 0x80000000
    nop

    # =================================================================
    # SUB - Test underflow case  
    # =================================================================
    lui $t0, 0x8000        # $t0 = 0x80000000 (MIN_INT)
    addi $t1, $zero, 1     # $t1 = 1
    sub $t2, $t0, $t1      # Underflow: 0x7FFFFFFF
    nop

    # =================================================================
    # AND - Test zero case
    # =================================================================
    addi $t0, $zero, 0xFF  # $t0 = 0xFF
    and $t1, $t0, $zero    # $t1 = 0x00
    nop

    # =================================================================
    # OR - Test all ones case
    # =================================================================
    addi $t0, $zero, 0x0F  # $t0 = 0x0F
    or $t1, $t0, $zero     # $t1 = 0x0F (no change)
    nop

    # =================================================================
    # NOR - Test complement case
    # =================================================================
    addi $t0, $zero, 0xAA  # $t0 = 0xAA
    nor $t1, $t0, $zero    # $t1 = 0xFFFFFF55
    nop

    # =================================================================
    # XOR - Test toggle bits
    # =================================================================
    addi $t0, $zero, 0xFF  # $t0 = 0xFF
    xor $t1, $t0, $t0      # $t1 = 0x00 (self-XOR)
    nop

    # =================================================================
    # SLL - Test max shift
    # =================================================================
    addi $t0, $zero, 1     # $t0 = 1
    sll $t1, $t0, 31       # $t1 = 0x80000000
    nop

    # =================================================================
    # SRL - Test max shift with sign
    # =================================================================
    lui $t0, 0x8000        # $t0 = 0x80000000
    srl $t1, $t0, 31       # $t1 = 0x00000001
    nop

    # =================================================================
    # MULTU - Test multiply by zero
    # =================================================================
    addi $t0, $zero, 5     # $t0 = 5
    addi $t1, $zero, 0     # $t1 = 0
    multu $t0, $t1         # HI=0, LO=0
    nop

    # =================================================================
    # MFHI - Test move from HI
    # =================================================================
    lui $t0, 0x1000        # $t0 = 0x10000000
    lui $t1, 0x1000        # $t1 = 0x10000000
    multu $t0, $t1         # HI=0x01000000, LO=0x00000000
    mfhi $t2               # $t2 = 0x01000000
    nop

    # =================================================================
    # MFLO - Test move from LO
    # =================================================================
    addi $t0, $zero, 0x1000 # $t0 = 0x1000
    addi $t1, $zero, 0x1000 # $t1 = 0x1000
    multu $t0, $t1         # HI=0, LO=0x1000000
    mflo $t3               # $t3 = 0x1000000
    nop

    # =================================================================
    # SLT - Test signed comparison
    # =================================================================
    addi $t0, $zero, -1    # $t0 = -1
    addi $t1, $zero, 1     # $t1 = 1
    slt $t2, $t0, $t1      # $t2 = 1 (-1 < 1)
    nop

    # =================================================================
    # JR - Test jump to return
    # =================================================================
    jal jr_test_function   # Call function
    nop
    j after_jr
    nop

jr_test_function:
    nop
    jr $ra                 # Return from function
    nop

after_jr:
    nop

    # =================================================================
    # JALR - Test jump and link register
    # =================================================================    
    lui $t0, 0x0040        # Load target address
    ori $t0, $t0, 0x010C
    nop
    nop
    jalr $t1, $t0          # Jump and link
    nop
    j after_jalr
    nop

jalr_target:
    
    nop
    jr $t1                 # Return using saved address
    nop

after_jalr:
    nop

    # =================================================================
    # ADDI - Test negative immediate
    # =================================================================
    addi $t0, $zero, -5    # $t0 = -5
    nop

    # =================================================================
    # ANDI - Test zero extend
    # =================================================================
    addi $t0, $zero, 0xFF  # $t0 = 0xFF
    andi $t1, $t0, 0x0F    # $t1 = 0x0F
    nop

    # =================================================================
    # ORI - Test set bits
    # =================================================================
    addi $t0, $zero, 0x0F  # $t0 = 0x0F
    ori $t1, $t0, 0xF0     # $t1 = 0xFF
    nop

    # =================================================================
    # SLTI - Test signed comparison
    # =================================================================
    addi $t0, $zero, -1    # $t0 = -1
    slti $t1, $t0, 1       # $t1 = 1 (-1 < 1)
    nop

    # =================================================================
    # LUI - Test load upper immediate
    # =================================================================
    lui $t0, 0x1234        # $t0 = 0x12340000
    nop

    # =================================================================
    # SW - Test word store
    # =================================================================
    lui $t0, 0x1000         # 
    ori $t0, $t0, 0x0000    # $t0 = 0x10000000
    addi $t1, $zero, 0x1234 # $t1 = 0x00001234
    sw $t1, 0($t0)          # Store word
    nop

    # =================================================================
    # LW - Test word load
    # =================================================================
    lui $t0, 0x1000        # Load address
    ori $t0, $t0, 0x0000
    lw $t2, 0($t0)         # Load word
    nop

    # =================================================================
    # SB - Test byte store
    # =================================================================
    lui $t0, 0x1000         # 
    ori $t0, $t0, 0x0006    # $t2 = 0x10000006
    addi $t1, $zero, 0x80   # $t2 = 0x80
    sb $t1, 0($t0)          # Store byte
    nop

    # =================================================================
    # LB - Test byte load with sign extension
    # =================================================================
    lui $t0, 0x1000         # 
    ori $t0, $t0, 0x0006    # $t2 = 0x10000006
    lb $t2, 0($t0)          # Load byte
    nop

    # =================================================================
    # BEQ - Test branch taken
    # =================================================================
    addi $t0, $zero, 5     # $t0 = 5
    addi $t1, $zero, 5     # $t1 = 5
    nop
    nop
    beq $t0, $t1, beq_taken # Branch should be taken
    nop
    j beq_end
    nop

beq_taken:
    addi $t2, $zero, 1     # $t2 = 1 (branch taken)
    
beq_end:
    nop

    # =================================================================
    # BNE - Test branch not taken
    # =================================================================
    addi $t0, $zero, 5     # $t0 = 5
    addi $t1, $zero, 5     # $t1 = 5
    nop
    nop
    bne $t0, $t1, bne_taken # Branch should NOT be taken
    nop
    addi $t2, $zero, 1     # $t2 = 1 (branch not taken)
    j bne_end
    nop

bne_taken:
    addi $t2, $zero, 0     # $t2 = 0
    
bne_end:
    nop

    # =================================================================
    # J - Test unconditional jump
    # =================================================================
    j j_target
    nop
    addi $t0, $zero, 0     # This should be skipped
    nop
    
j_target:
    addi $t0, $zero, 1     # $t0 = 1
    nop

    # =================================================================
    # JAL - Test jump and link
    # =================================================================
    jal jal_function
    nop
    j end
    nop

jal_function:
    addi $t1, $zero, 3
    jr $ra                 # Return
    nop

end:
    nop
